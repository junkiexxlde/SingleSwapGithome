    const SURFACE_STYLE_STORAGE_KEY = 'surfaceStyle';

const breadcrumbPageMap = {
    'index.html': 'nav-home',
    'singleswap.html': 'nav-new-case',
    'overview.html': 'nav-overview',
    'assetmanagement.html': 'nav-assets',
    'monthlyinventory.html': 'nav-monthly-inventory'
};

function getCurrentPageName() {
    const path = window.location.pathname.split('/').pop() || 'index.html';
    return breadcrumbPageMap[path] || 'nav-home';
}

function renderTopbarBreadcrumb(lang) {
    const leftHost = document.querySelector('.topbar-left');
    if (!leftHost) {
        return;
    }

    const dict = navTranslations[lang] || navTranslations.de;
    const currentPageKey = getCurrentPageName();
    const crumbItems = [{ href: 'index.html', key: 'nav-home', current: currentPageKey === 'nav-home' }];

    if (currentPageKey !== 'nav-home') {
        const currentPath = window.location.pathname.split('/').pop() || 'index.html';
        crumbItems.push({ href: currentPath, key: currentPageKey, current: true });
    }

    const breadcrumbHtml = crumbItems.map((item, index) => {
        const label = dict[item.key] || item.key;
        const separator = index > 0 ? '<span class="topbar-breadcrumb-separator" aria-hidden="true">\\</span>' : '';
        if (item.current) {
            return `${separator}<span class="topbar-breadcrumb-current" aria-current="page">${label}</span>`;
        }
        return `${separator}<a class="topbar-breadcrumb-link" href="${item.href}">${label}</a>`;
    }).join('');

    let breadcrumbHost = leftHost.querySelector('.topbar-breadcrumb-host');
    if (!breadcrumbHost) {
        breadcrumbHost = document.createElement('nav');
        breadcrumbHost.className = 'topbar-breadcrumb topbar-breadcrumb-host';
        breadcrumbHost.setAttribute('aria-label', 'Breadcrumb');
        leftHost.appendChild(breadcrumbHost);
    }

    breadcrumbHost.innerHTML = breadcrumbHtml;
}

function setNavLanguage(lang) {
    const dict = navTranslations[lang] || navTranslations.de;

    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.textContent = dict[key];
        }
    });

    document.querySelectorAll('[data-i18n-titleattr]').forEach((el) => {
        const key = el.getAttribute('data-i18n-titleattr');
        if (dict[key]) {
            el.setAttribute('title', dict[key]);
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) {
            el.setAttribute('placeholder', dict[key]);
        }
    });

    const titleTag = document.querySelector('title[data-i18n-title]');
    if (titleTag) {
        const titleKey = titleTag.getAttribute('data-i18n-title');
        if (dict[titleKey]) {
            titleTag.textContent = dict[titleKey];
            document.title = dict[titleKey];
        }
    }

    renderTopbarBreadcrumb(lang);

    const deButton = document.getElementById('lang-de');
    const enButton = document.getElementById('lang-en');
    if (deButton && enButton) {
        deButton.classList.toggle('active', lang === 'de');
        enButton.classList.toggle('active', lang === 'en');
    }

    localStorage.setItem('language', lang);
    document.dispatchEvent(new CustomEvent('mdm-language-changed', { detail: { lang } }));
}

function setNavTheme(theme) {
    document.documentElement.setAttribute('data-theme', theme);
    const themeToggle = document.getElementById('theme-toggle');
    if (themeToggle) {
        themeToggle.textContent = theme === 'light' ? '🌙' : '☀️';
    }
    localStorage.setItem('theme', theme);
}

function setNavSurfaceStyle(style) {
    const nextStyle = style === 'pagination' ? 'pagination' : 'classic';
    document.documentElement.setAttribute('data-surface-style', nextStyle);
    localStorage.setItem(SURFACE_STYLE_STORAGE_KEY, nextStyle);

    const toggle = document.getElementById('appearanceSurfaceToggle');
    if (toggle) {
        toggle.checked = nextStyle === 'pagination';
    }

    document.dispatchEvent(new CustomEvent('mdm-surface-style-changed', { detail: { style: nextStyle } }));
}

function ensureAppearanceSettings() {
    const settingsMenu = document.getElementById('settings-menu');
    if (!settingsMenu) {
        return;
    }

    let host = settingsMenu.querySelector('.asset-settings-panel');
    if (!host) {
        settingsMenu.innerHTML = '';
        host = document.createElement('div');
        host.className = 'asset-settings-panel common-settings-panel';
        settingsMenu.appendChild(host);
    } else {
        settingsMenu.querySelector('[data-i18n="settings-placeholder"]')?.remove();
    }

    if (host.querySelector('#appearanceSettingsSection')) {
        return;
    }

    const section = document.createElement('section');
    section.id = 'appearanceSettingsSection';
    section.className = 'asset-settings-section appearance-settings-section';
    section.innerHTML = `
        <h3 data-i18n="appearance-settings-title">Darstellung</h3>
        <label class="appearance-toggle-row" for="appearanceSurfaceToggle">
            <input id="appearanceSurfaceToggle" class="appearance-toggle-input" type="checkbox">
            <span data-i18n="appearance-surface-toggle-label">Alternative Box-Optik</span>
        </label>
        <p class="appearance-settings-hint" data-i18n="appearance-surface-toggle-hint">Übernimmt die weichere Pagination-Optik für Karten und Boxen.</p>
    `;
    host.prepend(section);

    const toggle = section.querySelector('#appearanceSurfaceToggle');
    if (toggle) {
        toggle.addEventListener('change', () => {
            setNavSurfaceStyle(toggle.checked ? 'pagination' : 'classic');
        });
    }
}

function closeNavDrawer() {
    const drawer = document.getElementById('nav-drawer');
    const backdrop = document.getElementById('menu-backdrop');
    if (drawer) {
        drawer.classList.remove('open');
        drawer.setAttribute('aria-hidden', 'true');
    }
    if (backdrop) {
        backdrop.classList.add('hidden');
    }
}

function toggleNavDrawer() {
    const drawer = document.getElementById('nav-drawer');
    const backdrop = document.getElementById('menu-backdrop');
    if (!drawer || !backdrop) {
        return;
    }

    const isOpen = drawer.classList.toggle('open');
    drawer.setAttribute('aria-hidden', isOpen ? 'false' : 'true');
    backdrop.classList.toggle('hidden', !isOpen);
    if (isOpen) {
        document.getElementById('settings-menu')?.classList.add('hidden');
    }
}

function toggleSettingsMenu() {
    const settingsMenu = document.getElementById('settings-menu');
    if (!settingsMenu) {
        return;
    }

    const shouldOpen = settingsMenu.classList.contains('hidden');
    settingsMenu.classList.toggle('hidden', !shouldOpen);
    if (shouldOpen) {
        closeNavDrawer();
    }
}

function isWebOriginSupported() {
    return window.location.protocol === 'http:' || window.location.protocol === 'https:';
}

async function requestPersistentStorage() {
    if (!isWebOriginSupported()) {
        console.info('[Storage] Skipped persistent storage request on non-http origin. Use localhost for shared browser storage.');
        return;
    }

    if (!('storage' in navigator)) {
        return;
    }

    if (typeof navigator.storage.persisted !== 'function' || typeof navigator.storage.persist !== 'function') {
        return;
    }

    try {
        const isPersisted = await navigator.storage.persisted();
        if (isPersisted) {
            console.log('[Storage] Persistent storage already enabled.');
            return;
        }

        const granted = await navigator.storage.persist();
        if (granted) {
            console.log('[Storage] Persistent storage enabled.');
        } else {
            console.warn('[Storage] Persistent storage request was not granted by the browser.');
        }
    } catch (error) {
        console.warn('[Storage] Persistent storage request failed:', error);
    }
}

(function initNavigationShell() {
    const prefersDarkScheme = window.matchMedia('(prefers-color-scheme: dark)');
    const initialTheme = localStorage.getItem('theme') || (prefersDarkScheme.matches ? 'dark' : 'light');
    const initialSurfaceStyle = localStorage.getItem(SURFACE_STYLE_STORAGE_KEY) || 'classic';
    const initialLanguage = localStorage.getItem('language') || 'de';

    ensureAppearanceSettings();
    setNavTheme(initialTheme);
    setNavSurfaceStyle(initialSurfaceStyle);
    setNavLanguage(initialLanguage);
    requestPersistentStorage();

    const deButton = document.getElementById('lang-de');
    const enButton = document.getElementById('lang-en');
    const themeToggle = document.getElementById('theme-toggle');
    const menuToggle = document.getElementById('menu-toggle');
    const menuBackdrop = document.getElementById('menu-backdrop');
    const settingsToggle = document.getElementById('settings-toggle');
    const drawerLinks = document.querySelectorAll('.drawer-nav a');

    if (deButton) {
        deButton.addEventListener('click', () => setNavLanguage('de'));
    }
    if (enButton) {
        enButton.addEventListener('click', () => setNavLanguage('en'));
    }
    if (themeToggle) {
        themeToggle.addEventListener('click', () => {
            const currentTheme = document.documentElement.getAttribute('data-theme') || 'light';
            setNavTheme(currentTheme === 'light' ? 'dark' : 'light');
        });
    }
    if (menuToggle) {
        menuToggle.addEventListener('click', toggleNavDrawer);
    }
    if (menuBackdrop) {
        menuBackdrop.addEventListener('click', closeNavDrawer);
    }
    if (settingsToggle) {
        settingsToggle.addEventListener('click', toggleSettingsMenu);
    }
    drawerLinks.forEach((link) => {
        link.addEventListener('click', closeNavDrawer);
    });

    document.addEventListener('click', (event) => {
        const settingsMenu = document.getElementById('settings-menu');
        if (!settingsMenu || settingsMenu.classList.contains('hidden')) {
            return;
        }

        if (!settingsMenu.contains(event.target) && event.target !== settingsToggle) {
            settingsMenu.classList.add('hidden');
        }
    });

    window.addEventListener('storage', (event) => {
        if (event.key === 'theme' && event.newValue) {
            setNavTheme(event.newValue);
        }
        if (event.key === 'language' && event.newValue) {
            setNavLanguage(event.newValue);
        }
        if (event.key === SURFACE_STYLE_STORAGE_KEY) {
            setNavSurfaceStyle(event.newValue || 'classic');
        }
    });
})();

// Service Worker registration (shared across all pages via navigation.js)
if ('serviceWorker' in navigator && isWebOriginSupported()) {
    window.addEventListener('load', () => {
        navigator.serviceWorker.register('./service-worker.js')
            .then(registration => {
                console.log('[SW] Registered, scope:', registration.scope);
            })
            .catch(err => console.error('[SW] Registration failed:', err));

        // Show a non-blocking reload banner when the SW_UPDATED message arrives
        navigator.serviceWorker.addEventListener('message', event => {
            if (event.data && event.data.type === 'SW_UPDATED') {
                showSwUpdateBanner(event.data.version);
            }
        });
    });
} else if ('serviceWorker' in navigator) {
    console.info('[SW] Skipped service worker registration on non-http origin. Open via localhost for full app functionality.');
}

function showSwUpdateBanner(version) {
    if (document.getElementById('swUpdateBanner')) return;
    const lang = localStorage.getItem('language') || 'de';
    const text = lang === 'en'
        ? `App updated to version ${version}. Reload for the latest version.`
        : `App auf Version ${version} aktualisiert. Seite neu laden für die aktuelle Version.`;
    const btnLabel = lang === 'en' ? 'Reload' : 'Neu laden';
    const banner = document.createElement('div');
    banner.id = 'swUpdateBanner';
    banner.className = 'sw-update-banner';
    banner.innerHTML = `
        <span class="sw-update-text">${text}</span>
        <button class="sw-update-reload button secondary" type="button">${btnLabel}</button>
    `;
    banner.querySelector('.sw-update-reload').addEventListener('click', () => {
        window.location.reload();
    });
    document.body.appendChild(banner);
}
